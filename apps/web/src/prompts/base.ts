/**
 * 把一段【角色设定】与共享块拼成完整系统提示词。
 * 这就是后台会暴露给管理员编辑的完整文本（默认值由 roster 生成）。
 */
export function assembleSystemPrompt(persona: string, domain: Domain): string {
  return [
    persona.trim(),
    '',
    DOMAIN_CONTEXT[domain],
    '',
    PRINCIPLES,
    '',
    STAGE_GUIDE,
    '',
    renderSkillCatalog(),
    '',
    OPTION_QUALITY,
    '',
    DELIVERABLES_GUIDE,
    '',
    JSON_CONTRACT,
  ].join('\n')
}

// ── 团队提示词：一个主搭子领全程 + 3 位专家按情况出场（用户不用选）──────────────────
export interface TeamMember { name: string; title: string; strengths: string[]; persona: string; domain: Domain }

export function assembleTeamPrompt(members: TeamMember[]): string {
  const host = members[0]
  const teamLines = members.map((m, i) => `${i + 1}. ${m.name}（${m.title}）：${m.strengths.join(' / ')}`)
  const teamBlock = `\
【你是谁 · 团队】
你是这个产品团队的${host.name}，也是这场对话的主持人。整场对话从头到尾都是你在跟用户聊——用户只跟你一个人打交道，不必自己在几个岗位里挑。
你身后有一支专业团队，遇到对应的事，就切到那个岗位的专业视角来答：
${teamLines.join('\n')}
路由（心里走，不用报流程）：聊"要做什么 / 给谁用 / 要不要做" → 你自己（产品经理）主导；聊"怎么做 / 用什么 / 地基牢不牢 / 多少钱多久" → 切技术架构师视角；聊"好不好用 / 会不会用晕" → 带上体验设计师视角；聊"拆步骤 / 验收 / 怎么交付" → 上交付负责人视角。
表达：需要某个岗位出场时，用一句自然、专业的话带出来（例："这块我请我们的技术架构师来看——…"，或"从好用度的角度，体验设计师会提醒你…"），然后就用那个岗位的专业和口吻作答。不要用人名，用岗位称呼；也别让用户感觉在跟四个机器人分别对话，而是一支懂行的团队在协作。始终是你在主持。`
  return [
    host.persona.trim(),
    '',
    teamBlock,
    '',
    DOMAIN_CONTEXT[host.domain],
    '',
    PRINCIPLES,
    '',
    STAGE_GUIDE,
    '',
    renderSkillCatalog(),
    '',
    OPTION_QUALITY,
    '',
    DELIVERABLES_GUIDE,
    '',
    JSON_CONTRACT,
  ].join('\n')
}

