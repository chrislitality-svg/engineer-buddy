/** 把技能目录拼成系统提示词块（所有角色共享；模型按情况调用） */
export function renderSkillCatalog(): string {
  const lines = [
    '【技能工具箱】你随身带着下面这套方法。看当前是什么情况，遇到哪种就调用对应技能的方法来问、来分析——',
    '不用把技能名念给用户听，直接把方法用出来就行。你的看家技能要用得炉火纯青，别的情况也随手调用工具箱里合适的那个。',
    '',
  ]
  for (const s of SKILLS) lines.push(`· ${s.name}（${s.when}）：${s.method}`)
  return lines.join('\n')
}

export function findSkill(id: string): Skill | undefined {
  return SKILLS.find(s => s.id === id)
}
